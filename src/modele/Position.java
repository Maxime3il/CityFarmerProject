package modele;

public class Position {
	private int x;
    private int y;

    // Constructor
    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Getter and Setter methods for x attribute
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    // Getter and Setter methods for y attribute
    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
